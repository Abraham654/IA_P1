#los operadores son simbolos por los cuales en programacion se pueden hacer operaciones
#como se usa print va imprimir el resultado de las operaciones, en este caso ** es exponenciacion
print(2 ** 3)#aqui ambos numeros son enteros como el quese va a elevar y la elevacion por lo cual saldra 8 entero
print(2 ** 3.)#aqui la elevacion es flotante y saldra 8.0 por la elevacion flotante, fraccionario o decimal, etc
print(2. ** 3)#aqui el numero que se va elevar es el flotante y saldra 8.0 por eso mismo.
print(2. ** 3.)#aqui ambos son flotantes y seguira saliendo 8.0
